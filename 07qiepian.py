print('切片表示方式1,L[0:3]',L[0:3])
print('切片标识方式2,L=[0:2]',L[0:2])
L=list(range(100))
print('构造一个0-100的切片，并显示list(rang(100))',L)
print('切片代表从第几个元素开始，取到第几个为准，不包含后面的元素,L[-4:-2]',L[-4:-2])
#高级切片操作
#从1开始取到第三个数字，所有只有两个，省略代表从0开始，不包含后面的元素
print(L[1:3])
#负数切片，非常有用，-1代表最后一个数字，-2代表倒数第二个，以此类推
#下面的表达式代表从倒数第二个元素开始取，取到最后一个
print(L[-2:-1])
print(L[-2:])
print(L[-5:-2])
#切片带有步进的形式，步进放在最后一位，下面代表从0开始到10结束，每隔2位
print(L[:10:2])
print(L[:])
